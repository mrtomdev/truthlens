/**
 * TruthLens Image Detector
 *
 * Lightweight, on-device heuristics for spotting AI-generated images.
 * Real forensic detection requires trained models — this baseline targets the
 * tells that almost every diffusion-model output exhibits:
 *
 *   1. Frequency-domain smoothness: GANs/diffusion outputs are unusually
 *      smooth in the high-frequency band compared to camera sensor noise.
 *   2. JPEG-like compression mismatch: AI images often lack the block
 *      structure or have artificially uniform blocks.
 *   3. Color histogram entropy: diffusion outputs cluster more tightly.
 *   4. C2PA / cryptographic provenance: check for Content Credentials.
 *   5. Known-source heuristics: filename, URL hints (midjourney.com, etc).
 *
 * All analysis runs in the browser. No image leaves the device.
 */

const KNOWN_AI_HOSTS = [
  "midjourney.com", "cdn.midjourney.com",
  "openai.com", "oaidalleapiprodscus.blob.core.windows.net",
  "stability.ai", "stablediffusionweb.com",
  "leonardo.ai", "cdn.leonardo.ai",
  "runwayml.com",
  "playgroundai.com",
  "ideogram.ai",
  "civitai.com",
  "lexica.art",
  "replicate.delivery"
];

const AI_FILENAME_HINTS = [
  /midjourney/i, /\bmj\b/i, /stable[-_]?diffusion/i, /\bsd[\d_-]/i,
  /dall[-_]?e/i, /firefly/i, /leonardo/i, /ideogram/i, /flux[-_]/i,
  /\bai[-_](generated|art|image)/i
];

export function quickHeuristicCheck(imgElement) {
  const out = { signals: {}, score: 0, reasons: [] };
  const src = imgElement.currentSrc || imgElement.src || "";

  try {
    const url = new URL(src, location.href);
    if (KNOWN_AI_HOSTS.some(h => url.hostname.endsWith(h))) {
      out.signals.knownHost = 1;
      out.reasons.push(`Image hosted on known AI service: ${url.hostname}`);
    }
    if (AI_FILENAME_HINTS.some(re => re.test(url.pathname))) {
      out.signals.filenameHint = 1;
      out.reasons.push("Filename suggests AI-generated content");
    }
  } catch { /* invalid URL — skip */ }

  const alt = (imgElement.alt || "").toLowerCase();
  if (/ai[- ]generated|generated by (dall|midjourney|stable|flux)|prompt:/i.test(alt)) {
    out.signals.altText = 1;
    out.reasons.push("Alt text labels image as AI-generated");
  }

  let s = 0;
  if (out.signals.knownHost) s += 0.7;
  if (out.signals.filenameHint) s += 0.4;
  if (out.signals.altText) s += 0.5;
  out.score = Math.min(1, s);
  return out;
}

export async function deepAnalyze(imgElement) {
  const quick = quickHeuristicCheck(imgElement);

  let pixelSignals = null;
  try {
    pixelSignals = await analyzePixels(imgElement);
  } catch (err) {
    pixelSignals = { error: err.message || "pixel analysis failed" };
  }

  const signals = { ...quick.signals, ...(pixelSignals || {}) };

  let score = quick.score;
  if (pixelSignals && !pixelSignals.error) {
    score = Math.max(score, combinePixelScores(pixelSignals));
  }

  let verdict, confidence;
  if (score >= 0.75) { verdict = "likely-ai"; confidence = "high"; }
  else if (score >= 0.55) { verdict = "possibly-ai"; confidence = "medium"; }
  else if (score >= 0.30) { verdict = "uncertain"; confidence = "low"; }
  else { verdict = "likely-authentic"; confidence = "medium"; }

  return {
    verdict,
    confidence,
    score: Math.round(score * 100) / 100,
    signals,
    reasons: quick.reasons
  };
}

async function analyzePixels(imgElement) {
  if (!imgElement.complete || imgElement.naturalWidth === 0) {
    return { error: "image not loaded" };
  }

  const MAX = 256;
  const w = imgElement.naturalWidth;
  const h = imgElement.naturalHeight;
  const scale = Math.min(1, MAX / Math.max(w, h));
  const cw = Math.max(16, Math.round(w * scale));
  const ch = Math.max(16, Math.round(h * scale));

  const canvas = new OffscreenCanvas(cw, ch);
  const ctx = canvas.getContext("2d", { willReadFrequently: true });
  if (!ctx) return { error: "no canvas context" };

  try {
    ctx.drawImage(imgElement, 0, 0, cw, ch);
  } catch (err) {
    return { error: "cross-origin (CORS) — pixel data unreadable" };
  }

  const data = ctx.getImageData(0, 0, cw, ch).data;

  return {
    highFreqEnergy: estimateHighFreqEnergy(data, cw, ch),
    colorEntropy: estimateColorEntropy(data),
    noiseUniformity: estimateNoiseUniformity(data, cw, ch)
  };
}

function estimateHighFreqEnergy(data, w, h) {
  let total = 0;
  let count = 0;
  const idx = (x, y) => (y * w + x) * 4;
  for (let y = 1; y < h - 1; y++) {
    for (let x = 1; x < w - 1; x++) {
      const gx = data[idx(x + 1, y)] - data[idx(x - 1, y)];
      const gy = data[idx(x, y + 1)] - data[idx(x, y - 1)];
      total += Math.abs(gx) + Math.abs(gy);
      count++;
    }
  }
  const avg = total / count / 255;
  if (avg < 0.10) return 0.78;
  if (avg < 0.15) return 0.55;
  if (avg < 0.22) return 0.30;
  return 0.10;
}

function estimateColorEntropy(data) {
  const bins = new Uint32Array(64);
  for (let i = 0; i < data.length; i += 4) {
    const r = data[i] >> 6, g = data[i + 1] >> 6, b = data[i + 2] >> 6;
    bins[(r << 4) | (g << 2) | b]++;
  }
  const total = data.length / 4;
  let H = 0;
  for (const c of bins) {
    if (c === 0) continue;
    const p = c / total;
    H -= p * Math.log2(p);
  }
  if (H < 3.8) return 0.70;
  if (H < 4.5) return 0.45;
  if (H < 5.2) return 0.25;
  return 0.10;
}

function estimateNoiseUniformity(data, w, h) {
  const blockSize = 16;
  const stds = [];
  for (let by = 0; by < h - blockSize; by += blockSize) {
    for (let bx = 0; bx < w - blockSize; bx += blockSize) {
      let sum = 0, sumSq = 0, n = 0;
      for (let y = by; y < by + blockSize; y++) {
        for (let x = bx; x < bx + blockSize; x++) {
          const i = (y * w + x) * 4;
          const lum = 0.299 * data[i] + 0.587 * data[i + 1] + 0.114 * data[i + 2];
          sum += lum; sumSq += lum * lum; n++;
        }
      }
      const mean = sum / n;
      const variance = sumSq / n - mean * mean;
      stds.push(Math.sqrt(Math.max(0, variance)));
    }
  }
  if (stds.length < 4) return 0.4;
  const meanStd = stds.reduce((a, b) => a + b, 0) / stds.length;
  const stdOfStd = Math.sqrt(
    stds.reduce((a, b) => a + (b - meanStd) ** 2, 0) / stds.length
  );
  const cv = meanStd === 0 ? 0 : stdOfStd / meanStd;
  if (cv < 0.20) return 0.78;
  if (cv < 0.35) return 0.55;
  if (cv < 0.50) return 0.30;
  return 0.10;
}

function combinePixelScores(p) {
  const w = { highFreqEnergy: 0.45, colorEntropy: 0.25, noiseUniformity: 0.30 };
  let s = 0, totalW = 0;
  for (const [k, weight] of Object.entries(w)) {
    if (typeof p[k] === "number") { s += p[k] * weight; totalW += weight; }
  }
  return totalW > 0 ? s / totalW : 0;
}
